  // ── Shared helpers ────────────────────────────────────────────────────────
  static pw.Widget _banner(String title, String subtitle) =>
    pw.Container(
      width: double.infinity,
      padding: const pw.EdgeInsets.fromLTRB(36, 30, 36, 26),
      decoration: const pw.BoxDecoration(color: _primary),
      child: pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
        pw.Text("SKILL RENTING APP  |  ADMIN",
            style: pw.TextStyle(fontSize: 8, color: _white, fontWeight: pw.FontWeight.bold, letterSpacing: 2)),
        pw.SizedBox(height: 6),
        pw.Text(title,
            style: pw.TextStyle(fontSize: 24, fontWeight: pw.FontWeight.bold, color: _white)),
        pw.SizedBox(height: 4),
        pw.Text(subtitle, style: const pw.TextStyle(fontSize: 12, color: _white)),
      ]),
    );

  static pw.Widget _card(String title, List<pw.Widget> rows) =>
    pw.Container(
      padding: const pw.EdgeInsets.all(12),
      decoration: pw.BoxDecoration(
        color: _surface,
        borderRadius: pw.BorderRadius.circular(4),
        border: pw.Border.all(color: _greyLight, width: 0.5),
      ),
      child: pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
        pw.Text(title, style: pw.TextStyle(fontSize: 7, fontWeight: pw.FontWeight.bold, color: _primary, letterSpacing: 1.2)),
        pw.SizedBox(height: 8),
        ...rows,
      ]),
    );

  static pw.Widget _kv(String label, String value) =>
    pw.Padding(padding: const pw.EdgeInsets.only(bottom: 5), child:
      pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
        pw.Text(label, style: const pw.TextStyle(fontSize: 7.5, color: _greyMid)),
        pw.Text(value, style: pw.TextStyle(fontSize: 9, fontWeight: pw.FontWeight.bold, color: _black)),
      ]));

  static pw.Widget _stat(String label, String value, PdfColor color) =>
    pw.Expanded(child: pw.Container(
      padding: const pw.EdgeInsets.symmetric(horizontal: 12, vertical: 10),
      decoration: pw.BoxDecoration(color: color, borderRadius: pw.BorderRadius.circular(6)),
      child: pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
        pw.Text(label, style: pw.TextStyle(fontSize: 7, color: _white, letterSpacing: 0.8)),
        pw.SizedBox(height: 4),
        pw.Text(value, style: pw.TextStyle(fontSize: 15, fontWeight: pw.FontWeight.bold, color: _white)),
      ]),
    ));

  static pw.Widget _hc(String t, pw.TextStyle s) =>
    pw.Padding(padding: const pw.EdgeInsets.fromLTRB(6,7,6,7), child: pw.Text(t, style: s));

  static pw.Widget _dc(String t, pw.TextStyle s) =>
    pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child: pw.Text(t, style: s));

  static pw.Widget _slotDc(Map b, pw.TextStyle cs, pw.TextStyle gs) =>
    _slotDcRaw(_parseDate(b["startDate"]), _parseDate(b["endDate"]),
        b["pricingUnit"]?.toString() ?? b["feeUnit"]?.toString() ?? "hour", cs, gs);

  static pw.Widget _slotDcRaw(DateTime start, DateTime end, String unit,
      pw.TextStyle cs, pw.TextStyle gs) {
    final date = _fmtDate(start);
    final time = unit == "hour"
        ? "${start.hour.toString().padLeft(2,'0')}:${start.minute.toString().padLeft(2,'0')} - ${end.hour.toString().padLeft(2,'0')}:${end.minute.toString().padLeft(2,'0')}"
        : "to ${_fmtDate(end)}";
    return pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child:
      pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
        pw.Text(date, style: pw.TextStyle(fontSize: 7.5, fontWeight: pw.FontWeight.bold, color: _black)),
        pw.Text(time, style: gs),
      ]));
  }

  static pw.Widget _statusBadge(String status) {
    final label = _statusLabel(status);
    final color = _statusColor(status);
    final bg    = _statusBg(status);
    return pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child:
      pw.Container(
        padding: const pw.EdgeInsets.symmetric(horizontal: 4, vertical: 3),
        decoration: pw.BoxDecoration(color: bg, borderRadius: pw.BorderRadius.circular(3),
            border: pw.Border.all(color: color, width: 0.5)),
        child: pw.Text(label, style: pw.TextStyle(fontSize: 7, color: color, fontWeight: pw.FontWeight.bold)),
      ));
  }

  static pw.Widget _feeDc(Map b, pw.TextStyle cs) {
    final status = b["status"]?.toString() ?? "";
    String text; pw.TextStyle style;
    if (status == "rejected") {
      text = "NIL"; style = pw.TextStyle(fontSize: 7.5, color: _red, fontWeight: pw.FontWeight.bold);
    } else if (status == "accepted" || status == "in_progress") {
      text = "Not Calculated"; style = pw.TextStyle(fontSize: 7.5, color: _greyMid);
    } else {
      final fee   = (b["fee"]   as num?)?.toDouble() ?? 0;
      final extra = (b["extraCharges"] as num?)?.toDouble() ?? 0;
      final total = (b["totalAmount"]  as num?)?.toDouble() ?? fee;
      text = "Base: Rs. ${fee.toStringAsFixed(0)}";
      if (extra > 0) text += "\nExtra: Rs. ${extra.toStringAsFixed(0)}";
      text += "\nTotal: Rs. ${total.toStringAsFixed(0)}";
      style = cs;
    }
    return pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child: pw.Text(text, style: style));
  }

  static pw.Widget _payDc(Map b, pw.TextStyle cs) {
    final status  = b["status"]?.toString() ?? "";
    final payment = b["paymentStatus"]?.toString() ?? "";
    String text; pw.TextStyle style;
    if (status == "rejected") {
      text = "-"; style = pw.TextStyle(fontSize: 7.5, color: _greyMid);
    } else if (status == "accepted" || status == "in_progress") {
      text = "Pending"; style = pw.TextStyle(fontSize: 7.5, color: _warn, fontWeight: pw.FontWeight.bold);
    } else if (payment == "paid") {
      text = "Received"; style = pw.TextStyle(fontSize: 7.5, color: _accent, fontWeight: pw.FontWeight.bold);
    } else {
      text = "Pending"; style = pw.TextStyle(fontSize: 7.5, color: _warn, fontWeight: pw.FontWeight.bold);
    }
    return pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child: pw.Text(text, style: style));
  }

  static String _statusLabel(String s) {
    switch (s) {
      case "accepted":    return "Accepted";
      case "in_progress": return "In Progress";
      case "completed":   return "Completed";
      case "rejected":    return "Rejected";
      case "cancelled":   return "Cancelled";
      default:            return s;
    }
  }

  static PdfColor _statusColor(String s) {
    switch (s) {
      case "accepted":    return const PdfColor.fromInt(0xFF1565C0);
      case "in_progress": return const PdfColor.fromInt(0xFF6A1B9A);
      case "completed":   return const PdfColor.fromInt(0xFF2E7D32);
      case "rejected":    return const PdfColor.fromInt(0xFFC62828);
      default:            return const PdfColor.fromInt(0xFF757575);
    }
  }

  static PdfColor _statusBg(String s) {
    switch (s) {
      case "accepted":    return const PdfColor.fromInt(0xFFE3F2FD);
      case "in_progress": return const PdfColor.fromInt(0xFFF3E5F5);
      case "completed":   return const PdfColor.fromInt(0xFFE8F5E9);
      case "rejected":    return const PdfColor.fromInt(0xFFFFEBEE);
      default:            return const PdfColor.fromInt(0xFFF5F5F5);
    }
  }

  static DateTime _parseDate(dynamic d) {
    if (d == null) return DateTime.now();
    return DateTime.tryParse(d.toString())?.toLocal() ?? DateTime.now();
  }

  static String _fmtDate(DateTime d) =>
      "${d.day.toString().padLeft(2,'0')}/${d.month.toString().padLeft(2,'0')}/${d.year}";

  static String _fmtDt(DateTime d) =>
      "${_fmtDate(d)}  ${d.hour.toString().padLeft(2,'0')}:${d.minute.toString().padLeft(2,'0')}";

