import 'dart:typed_data';
import 'package:pdf/pdf.dart';
import 'package:pdf/widgets.dart' as pw;

// ─────────────────────────────────────────────────────────────────────────────
// All text uses ASCII-safe characters only (Helvetica compatible)
// ─────────────────────────────────────────────────────────────────────────────

class AdminPdfBuilder {
  static const _primary   = PdfColor.fromInt(0xFF1A237E);
  static const _accent    = PdfColor.fromInt(0xFF388E3C);
  static const _warn      = PdfColor.fromInt(0xFFE65100);
  static const _red       = PdfColor.fromInt(0xFFC62828);
  static const _surface   = PdfColor.fromInt(0xFFF5F5F5);
  static const _greyLight = PdfColor.fromInt(0xFFE0E0E0);
  static const _greyMid   = PdfColor.fromInt(0xFF9E9E9E);
  static const _black     = PdfColor.fromInt(0xFF212121);
  static const _white     = PdfColors.white;

  // ── USER activity report ──────────────────────────────────────────────────
  static Future<Uint8List> buildUserReport(Map<String, dynamic> data) async {
    final pdf = pw.Document(
      theme: pw.ThemeData.withFont(
        base: pw.Font.helvetica(), bold: pw.Font.helveticaBold()),
    );

    final bookings   = (data["bookings"]  as List? ?? []);
    final totalSpent = (data["totalSpent"] as num?)?.toDouble() ?? 0;
    final user       = {
      "name":    data["userName"]    ?? "",
      "phone":   data["userPhone"]   ?? "",
      "email":   data["userEmail"]   ?? "",
      "address": data["userAddress"] ?? {},
    };
    final dateFrom = _parseDate(data["dateFrom"]);
    final dateTo   = _parseDate(data["dateTo"]);

    // Cover
    pdf.addPage(pw.Page(
      pageFormat: PdfPageFormat.a4,
      margin: const pw.EdgeInsets.all(0),
      build: (_) => pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          _banner("CUSTOMER ACTIVITY REPORT",
              "${_fmtDate(dateFrom)} - ${_fmtDate(dateTo)}"),
          pw.Padding(
            padding: const pw.EdgeInsets.all(32),
            child: pw.Column(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: [
                pw.Row(children: [
                  pw.Expanded(child: _card("CUSTOMER", [
                    _kv("Name",  user["name"].toString()),
                    _kv("Phone", user["phone"].toString()),
                    _kv("Email", user["email"].toString()),
                  ])),
                  pw.SizedBox(width: 16),
                  pw.Expanded(child: _card("ADDRESS", [
                    _kv("Locality", (user["address"] as Map)["locality"]?.toString() ?? "-"),
                    _kv("District", (user["address"] as Map)["district"]?.toString() ?? "-"),
                    _kv("Pincode",  (user["address"] as Map)["pincode"]?.toString()  ?? "-"),
                  ])),
                  pw.SizedBox(width: 16),
                  pw.Expanded(child: _card("SUMMARY", [
                    _kv("Total Bookings", "${bookings.length}"),
                    _kv("Total Spent",    "Rs. ${totalSpent.toStringAsFixed(0)}"),
                    _kv("Period",         "${_fmtDate(dateFrom)} to ${_fmtDate(dateTo)}"),
                  ])),
                ]),
                pw.SizedBox(height: 20),
                pw.Row(children: [
                  _stat("BOOKINGS",     "${bookings.length}",                        _primary),
                  pw.SizedBox(width: 10),
                  _stat("TOTAL SPENT",  "Rs. ${totalSpent.toStringAsFixed(0)}",      _accent),
                  pw.SizedBox(width: 10),
                  _stat("COMPLETED",    "${bookings.where((b) => b["status"] == "completed").length}", _accent),
                  pw.SizedBox(width: 10),
                  _stat("PENDING",      "${bookings.where((b) => b["paymentStatus"] != "paid" && b["status"] == "completed").length}", _warn),
                ]),
                pw.SizedBox(height: 20),
                pw.Divider(color: _greyLight),
                pw.SizedBox(height: 8),
                pw.Text("Generated by Skill Renting App Admin Panel",
                    style: const pw.TextStyle(fontSize: 8, color: _greyMid)),
              ],
            ),
          ),
        ],
      ),
    ));

    // Booking rows (MultiPage landscape)
    if (bookings.isNotEmpty) {
      pdf.addPage(_userBookingMultiPage(user, bookings, dateFrom, dateTo));
    }

    return pdf.save();
  }

  static pw.MultiPage _userBookingMultiPage(
    Map user, List bookings, DateTime from, DateTime to) {

    const colWidths = {
      0: pw.FlexColumnWidth(2.0), // Service
      1: pw.FlexColumnWidth(2.0), // Provider
      2: pw.FlexColumnWidth(2.4), // Description
      3: pw.FlexColumnWidth(1.8), // Date & Slot
      4: pw.FlexColumnWidth(1.2), // Status
      5: pw.FlexColumnWidth(1.8), // Fee
      6: pw.FlexColumnWidth(1.2), // Payment
      7: pw.FlexColumnWidth(1.4), // Rating
    };
    final hs = pw.TextStyle(fontSize: 8, fontWeight: pw.FontWeight.bold, color: _white);
    final cs = pw.TextStyle(fontSize: 7.5, color: _black);
    final gs = pw.TextStyle(fontSize: 7,   color: _greyMid);

    return pw.MultiPage(
      pageFormat: PdfPageFormat.a4.landscape,
      margin: const pw.EdgeInsets.fromLTRB(24, 24, 24, 32),
      header: (ctx) => pw.Column(children: [
        pw.Row(
          mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
          children: [
            pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
              pw.Text("BOOKING HISTORY",
                  style: pw.TextStyle(fontSize: 13, fontWeight: pw.FontWeight.bold, color: _primary)),
              pw.Text("${user["name"]}  |  ${_fmtDate(from)} - ${_fmtDate(to)}",
                  style: const pw.TextStyle(fontSize: 8, color: _greyMid)),
            ]),
            pw.Text("Page ${ctx.pageNumber} of ${ctx.pagesCount}",
                style: const pw.TextStyle(fontSize: 8, color: _greyMid)),
          ],
        ),
        pw.SizedBox(height: 8),
        pw.Table(columnWidths: colWidths, children: [
          pw.TableRow(
            decoration: const pw.BoxDecoration(color: _primary),
            children: ["Service","Provider","Description","Date & Slot","Status","Fee (Rs.)","Payment","Rating"]
                .map((h) => _hc(h, hs)).toList(),
          ),
        ]),
      ]),
      footer: (ctx) => pw.Align(
        alignment: pw.Alignment.centerRight,
        child: pw.Text("Skill Renting App | Admin | Confidential",
            style: const pw.TextStyle(fontSize: 7, color: _greyMid)),
      ),
      build: (ctx) {
        final rows = bookings.asMap().entries.map((e) {
          final i = e.key;
          final b = Map<String, dynamic>.from(e.value as Map);
          final bg = i.isEven ? _white : _surface;
          final review = b["review"] as Map?;
          final ratingText = review == null ? "-"
              : "${List.generate(5, (i) => i < (review["rating"] as num).toInt() ? "*" : "-").join()} ${review["rating"]}/5";

          return pw.TableRow(
            decoration: pw.BoxDecoration(color: bg),
            children: [
              _dc(b["skillTitle"]?.toString() ?? "-",     cs),
              pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child:
                pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
                  pw.Text(b["providerName"]?.toString()  ?? "-", style: cs),
                  pw.Text(b["providerPhone"]?.toString() ?? "-", style: gs),
                ])),
              _dc((b["description"]?.toString().length ?? 0) > 60
                  ? "${b["description"].toString().substring(0,60)}..." : b["description"]?.toString() ?? "-", cs),
              _slotDc(b, cs, gs),
              _statusBadge(b["status"]?.toString() ?? ""),
              _feeDc(b, cs),
              _payDc(b, cs),
              _dc(ratingText, cs),
            ],
          );
        }).toList();

        return [
          pw.Table(
            columnWidths: colWidths,
            border: pw.TableBorder.all(color: _greyLight, width: 0.4),
            children: rows,
          ),
          pw.SizedBox(height: 14),
          pw.Align(
            alignment: pw.Alignment.centerRight,
            child: pw.Container(
              padding: const pw.EdgeInsets.symmetric(horizontal: 16, vertical: 8),
              decoration: pw.BoxDecoration(color: _accent, borderRadius: pw.BorderRadius.circular(4)),
              child: pw.Text(
                "TOTAL SPENT:  Rs. ${(bookings.where((b) => (b as Map)["paymentStatus"] == "paid").fold<double>(0, (s,b) => s + ((b as Map)["totalAmount"] as num).toDouble())).toStringAsFixed(0)}",
                style: pw.TextStyle(fontSize: 11, fontWeight: pw.FontWeight.bold, color: _white),
              ),
            ),
          ),
        ];
      },
    );
  }

  // ── PROVIDER report ───────────────────────────────────────────────────────
  static Future<Uint8List> buildProviderReport(Map<String, dynamic> data) async {
    final pdf = pw.Document(
      theme: pw.ThemeData.withFont(
        base: pw.Font.helvetica(), bold: pw.Font.helveticaBold()),
    );

    final sections   = (data["skillSections"] as List? ?? []);
    final grandTotal = (data["grandTotal"] as num?)?.toDouble() ?? 0;
    final dateFrom   = _parseDate(data["dateFrom"]);
    final dateTo     = _parseDate(data["dateTo"]);

    // Cover
    pdf.addPage(pw.Page(
      pageFormat: PdfPageFormat.a4,
      margin: const pw.EdgeInsets.all(0),
      build: (_) => pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          _banner("PROVIDER ACTIVITY REPORT",
              "${_fmtDate(dateFrom)} - ${_fmtDate(dateTo)}"),
          pw.Padding(
            padding: const pw.EdgeInsets.all(32),
            child: pw.Column(
              crossAxisAlignment: pw.CrossAxisAlignment.start,
              children: [
                pw.Row(children: [
                  pw.Expanded(child: _card("PROVIDER", [
                    _kv("Name",  data["providerName"]?.toString()  ?? ""),
                    _kv("Phone", data["providerPhone"]?.toString() ?? ""),
                    _kv("Email", data["providerEmail"]?.toString() ?? ""),
                  ])),
                  pw.SizedBox(width: 16),
                  pw.Expanded(child: _card("SERVICES", [
                    _kv("Total Skills",  "${sections.length}"),
                    _kv("Report Period", "${_fmtDate(dateFrom)} to ${_fmtDate(dateTo)}"),
                  ])),
                  pw.SizedBox(width: 16),
                  pw.Expanded(child: _card("FINANCIALS", [
                    _kv("Grand Total Received", "Rs. ${grandTotal.toStringAsFixed(0)}"),
                    _kv("Total Bookings",
                        "${sections.fold<int>(0, (s, sec) => s + ((sec as Map)["bookings"] as List).length)}"),
                  ])),
                ]),
                pw.SizedBox(height: 20),
                // Per-skill summary rows
                pw.Text("SERVICE BREAKDOWN",
                    style: pw.TextStyle(fontSize: 10, fontWeight: pw.FontWeight.bold, color: _primary)),
                pw.SizedBox(height: 8),
                pw.Table(
                  border: pw.TableBorder.all(color: _greyLight, width: 0.5),
                  columnWidths: {
                    0: const pw.FlexColumnWidth(3),
                    1: const pw.FlexColumnWidth(2),
                    2: const pw.FlexColumnWidth(2),
                    3: const pw.FlexColumnWidth(2),
                  },
                  children: [
                    pw.TableRow(
                      decoration: const pw.BoxDecoration(color: _primary),
                      children: ["Service","Pricing","Bookings","Received"]
                          .map((h) => _hc(h, pw.TextStyle(fontSize: 8, fontWeight: pw.FontWeight.bold, color: _white)))
                          .toList(),
                    ),
                    ...sections.asMap().entries.map((e) {
                      final sec = Map<String, dynamic>.from(e.value as Map);
                      final bg = e.key.isEven ? _white : _surface;
                      return pw.TableRow(
                        decoration: pw.BoxDecoration(color: bg),
                        children: [
                          _dc(sec["skillTitle"]?.toString() ?? "", const pw.TextStyle(fontSize: 8)),
                          _dc("Rs. ${sec["pricePerUnit"]}/${sec["pricingUnit"]}", const pw.TextStyle(fontSize: 8)),
                          _dc("${(sec["bookings"] as List).length}", const pw.TextStyle(fontSize: 8)),
                          _dc("Rs. ${(sec["totalReceived"] as num).toStringAsFixed(0)}", const pw.TextStyle(fontSize: 8)),
                        ],
                      );
                    }),
                  ],
                ),
                pw.SizedBox(height: 16),
                pw.Divider(color: _greyLight),
                pw.SizedBox(height: 8),
                pw.Text("Generated by Skill Renting App Admin Panel",
                    style: const pw.TextStyle(fontSize: 8, color: _greyMid)),
              ],
            ),
          ),
        ],
      ),
    ));

    // One detail section per skill
    for (final sec in sections) {
      final section = Map<String, dynamic>.from(sec as Map);
      final bookings = (section["bookings"] as List? ?? []);
      final blocked  = (section["blockedSlots"] as List? ?? []);
      if (bookings.isEmpty && blocked.isEmpty) continue;

      if (bookings.isNotEmpty) {
        pdf.addPage(_providerSkillMultiPage(
          data["providerName"]?.toString() ?? "",
          section, dateFrom, dateTo,
        ));
      }
    }

    return pdf.save();
  }

