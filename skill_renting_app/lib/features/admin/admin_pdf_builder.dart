                ...(section["blockedSlots"] as List).asMap().entries.map((e) {
                  final bl = Map<String, dynamic>.from(e.value as Map);
                  return pw.TableRow(
                    decoration: pw.BoxDecoration(color: e.key.isEven ? _white : _surface),
                    children: [
                      _dc(_fmtDt(_parseDate(bl["startDate"])), const pw.TextStyle(fontSize: 7.5)),
