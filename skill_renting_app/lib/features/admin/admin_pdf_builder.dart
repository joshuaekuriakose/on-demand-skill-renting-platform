            final ratingText = review == null ? "-"
                : "${List.generate(5, (i) => i < (review["rating"] as num).toInt() ? "*" : "-").join()} ${review["rating"]}/5";

            return pw.TableRow(
              decoration: pw.BoxDecoration(color: bg),
