                    pw.Text(b["seekerName"]?.toString()  ?? "-", style: cs),
                    pw.Text(b["seekerPhone"]?.toString() ?? "-", style: gs),
                  ])),
                pw.Padding(padding: const pw.EdgeInsets.fromLTRB(5,6,5,6), child:
                  pw.Column(crossAxisAlignment: pw.CrossAxisAlignment.start, children: [
                    pw.Text(b["providerName"]?.toString()  ?? "-", style: cs),
                    pw.Text(b["providerPhone"]?.toString() ?? "-", style: gs),
                  ])),
                _dc((b["description"]?.toString().length ?? 0) > 60
                    ? "${b["description"].toString().substring(0,60)}..." : b["description"]?.toString() ?? "-", cs),
                _slotDcRaw(_parseDate(b["startDate"]), _parseDate(b["endDate"]),
                    b["pricingUnit"]?.toString() ?? "hour", cs, gs),
                _statusBadge(b["status"]?.toString() ?? ""),
                _feeDc(b, cs),
                _payDc(b, cs),
                _dc(ratingText, cs),
              ],
            );
          }).toList();

          return [
            pw.Table(columnWidths: colWidths,
                border: pw.TableBorder.all(color: _greyLight, width: 0.4),
                children: rows),
            pw.SizedBox(height: 14),
            pw.Align(alignment: pw.Alignment.centerRight,
              child: pw.Container(
                padding: const pw.EdgeInsets.symmetric(horizontal: 14, vertical: 8),
                decoration: pw.BoxDecoration(color: _accent, borderRadius: pw.BorderRadius.circular(4)),
                child: pw.Text("TOTAL RECEIVED:  Rs. ${totalReceived.toStringAsFixed(0)}",
                    style: pw.TextStyle(fontSize: 10, fontWeight: pw.FontWeight.bold, color: _white)),
              ),
            ),
          ];
        },
      ));
    }

    return pdf.save();
  }
