                _kv("Total Bookings", "${bookings.length}"),
                _kv("Total Received", "Rs. ${totalReceived.toStringAsFixed(0)}"),
                _kv("Completed",      "${bookings.where((b) => (b as Map)["status"] == "completed").length}"),
              ])),
            ]),
